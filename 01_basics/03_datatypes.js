/*JS has inbuilt datatypes
some of them are discussed below*/

//number => consist of integer values
let age=19
console.log(typeof(age))//number

//Strings => These holds character values or alphabets
let company_Name="J.P MORGAN CHASE"
console.log(typeof(company_Name))//string

//Bollean => These contain True/false values
let is_Indian=true
console.log(typeof(is_Indian))//boolean

//undefined=> value is not assigned or un defined
let email;
console.log(typeof(email))//Undefined

//Null => its a standalone value but its type is object
console.log(typeof(null))//Object

//Symbols => they are widely used for providing uniqueness